#!/usr/bin/env node
/**
 * Post-build patch: inject a Cloudflare Workers scheduled() handler into the
 * Nitro-generated Worker entry (.output/server/index.mjs).
 *
 * Nitro's cloudflare-module preset generates its own `export default { fetch(...) }`.
 * Cloudflare Workers Cron Triggers invoke `scheduled()` on the same export, but
 * Nitro does NOT generate one — so we inject it here after the build.
 *
 * Strategy:
 *   1. Read the generated entry
 *   2. Replace the final `export default {` with a named const `__nitroWorker`
 *   3. Re-export `__nitroWorker` spread + a `scheduled()` that self-calls the
 *      existing /api/cron/rate-check HTTP endpoint (CRON_SECRET-guarded), so all
 *      rate-check business logic stays in server.ts — a single source of truth.
 *
 * Usage:
 *   node scripts/patch-cf-scheduled.mjs
 *
 * Called by .github/workflows/deploy.yml immediately after `pnpm run build`.
 */

import { readFileSync, writeFileSync } from "node:fs";

const ENTRY = ".output/server/index.mjs";

let src;
try {
  src = readFileSync(ENTRY, "utf8");
} catch (e) {
  console.error("[patch-cf-scheduled] Could not read", ENTRY, "—", e.message);
  process.exit(1);
}

// Nitro cloudflare-module preset always ends the file with `export default { ... };`
// We find the LAST occurrence so we don't accidentally match internal re-exports.
const MARKER = "\nexport default {";
const lastIdx = src.lastIndexOf(MARKER);

if (lastIdx === -1) {
  console.warn(
    "[patch-cf-scheduled] WARNING: Could not locate `export default {` in Nitro output.",
    "Cron trigger will not work until the patch is updated to match the new output format.",
  );
  process.exit(0); // non-fatal — deploy still succeeds, just without scheduled()
}

// Split at the marker:
//   before  = everything up to (but not including) "\nexport default {"
//   after   = starting from "{" (the object literal, ending with "};")
const before = src.slice(0, lastIdx);
const after  = src.slice(lastIdx + "\nexport default".length); // keeps " { ... };"

const patched =
  before +
  // Rename original export to a const so we can spread it
  "\nconst __nitroWorker =" +
  after +
  // Append the combined export with scheduled() added
  `
// ── Cloudflare Workers Cron Trigger ──────────────────────────────────────────
// Injected by scripts/patch-cf-scheduled.mjs (post-build — not generated by Nitro).
// Configured via wrangler.toml: [env.production.triggers] crons = ["0 */6 * * *"]
//
// The handler self-calls /api/cron/rate-check so all rate-check logic lives in
// server.ts behind the CRON_SECRET guard — no duplicated business logic here.
export default {
  ...__nitroWorker,

  /**
   * Cloudflare Workers scheduled handler — fires every 6 hours via cron trigger.
   * @param {ScheduledEvent}    event
   * @param {Record<string,*>}  env   — CF Worker bindings (secrets, vars)
   * @param {ExecutionContext}  ctx
   */
  async scheduled(event, env, ctx) {
    // Seed globalThis.__env__ so Nitro's getEnv() works inside the fetch call.
    // Nitro's generated fetch handler does this automatically; scheduled() bypasses it.
    if (env) globalThis.__env__ = env;

    const cronSecret = env?.CRON_SECRET ?? globalThis.__env__?.CRON_SECRET ?? "";
    const appUrl     = env?.APP_URL     ?? globalThis.__env__?.APP_URL     ?? "https://7evencards.xyz";

    if (!cronSecret) {
      console.error("[Scheduled] CRON_SECRET not configured — aborting rate-check");
      return;
    }

    // Self-fetch: Workers can call their own routes via the public URL.
    // The CRON_SECRET header satisfies the same guard as the external-curl path,
    // so the logic truly stays in one place.
    ctx.waitUntil(
      fetch(appUrl + "/api/cron/rate-check", {
        method:  "POST",
        headers: {
          "x-cron-secret":     cronSecret,
          "x-cloudflare-cron": "1",
          "Content-Type":      "application/json",
        },
      })
        .then(r => console.info("[Scheduled] Rate-check HTTP", r.status))
        .catch(e => console.error("[Scheduled] Rate-check error:", e instanceof Error ? e.message : String(e)))
    );
  },
};
`;

writeFileSync(ENTRY, patched, "utf8");
console.log("[patch-cf-scheduled] ✓ scheduled() injected into", ENTRY);
